package week1.day2;

public class FindOddNumbers {

	public static void main(String[] args) {
		// To print odd numbers from 1 to 10
		
		for (int maxRange = 1; maxRange <=10; maxRange++) {
			if(maxRange%2==1) {
				System.out.println("The number " +maxRange +" is odd");
			}
			
		}

	}

}
